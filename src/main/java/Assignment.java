public class Assignment {
    private int soldierID;
    private int dutyID;

    public Assignment() {
    }

    public int getSoldierID() {
        return soldierID;
    }

    public int getDutyID() {
        return dutyID;
    }

    public void setDutyID(int dutyID) {
        this.dutyID = dutyID;
    }

    public void setSoldierID(int soldierID) {
        this.soldierID = soldierID;
    }
}
